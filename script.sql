DROP DATABASE IF EXISTS polynames;

CREATE DATABASE polynames;

USE polynames;

CREATE TABLE Partie (
    partieID INT AUTO_INCREMENT PRIMARY KEY,
    codePartie VARCHAR(255),
    maitreIntuition VARCHAR(255),
    maitreIndice VARCHAR(255),
    score INT,
    guess VARCHAR(255),
    indice VARCHAR(255),
    pourCombien INT
);

CREATE TABLE Couleur (
    couleurID INT AUTO_INCREMENT PRIMARY KEY,
    nom VARCHAR(255)
);

CREATE TABLE Carte (
    carteID INT AUTO_INCREMENT PRIMARY KEY,
    mot VARCHAR(255),
    couleurID INT,
    guessed BOOLEAN DEFAULT FALSE,
    posX INT,
    posY INT,
    FOREIGN KEY (couleurID) REFERENCES Couleur(couleurID)
);

CREATE TABLE Cartes_Choisies (
    partieID INT,
    carteID INT,
    FOREIGN KEY (partieID) REFERENCES Partie(partieID),
    FOREIGN KEY (carteID) REFERENCES Carte(carteID),
    PRIMARY KEY (partieID, carteID)
);

